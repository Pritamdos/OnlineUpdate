				<br><br>
				
				<span style="color:#ff9908;text-weight:bold"> 
					Introduction 
				</span>
				
				<br><br>
			
				&ensp; 1. The introductory paragraphs of your essay must clearly define the central 
				theme and issues of your paper. You should also mention the core keywords here 
				<br><br>
			
			
				&ensp; 2. The approach you wish to take in your essay should be clearly laid out 
				in the introduction along with the conclusion you aim to draw 
				<br><br>
				
				<span style="color:#ff9908;text-weight:bold"> 
					Conclusion 
				</span>
				
				<br><br>
			
				&ensp; 1. Your conclusion should be a compilation of the key arguments you’ve made 
				in your paper to put your point across 
				<br><br>
			
			
				&ensp; 2. The answer to the central question of your essay must also be made here 
				along with the process you used to arrive at this answer  
