				<span style="font-style:italic;font-weight:bold">
					Definition of Plagiarism and Why You Should Maintain Academic Integrity  
				</span>
		
				<div style="padding-top:10px">
					Plagiarism is the most heinous crime you can commit while writing your essay/assignment.
					Plagiarism can be defined as directly copying the work of another and crediting it as your
					own. You will be caught and punished for plagiarism if you-
				</div>
				
				<br>
				
				&ensp;&ensp;&ensp; • Submit a paper that was not written by you
				<br><br>
			
			
				&ensp;&ensp;&ensp; • Submit a paper under your name whose material was copied from another source, 
				whether entirely or partially    
				<br><br>
				
				&ensp;&ensp;&ensp; • Submit a paper with stolen ideas without giving due credit to the author concerned
				<br><br>
				
				<div style="padding-top:10px">
					Academic integrity is crucial if you wish to avoid plagiarism and its consequences. 
					Academic integrity can be referred to as the academic responsibility and honesty you 
					maintain while writing your papers. Both students and faculty alike should uphold and
					maintain academic integrity and strictly follow the rules it lays down. This means that
					giving credit to the author concerned whose ideas have been borrowed for assignment
					writing.
				</div>
				
