				<p class="text-font-sr">
					As a student, the thorough knowledge of multiple referencing styles is a must for you. Why? Because different subjects and different teachers may require you to cite in one form over the other. For example some professors may prefer Harvard while others may recommend Oxford. Similarly, if you are a student of science or medicine, you will be asked to cite all your articles in the Vancouver style. 
				</p>
				
				<p class="text-font-sr">
					The Vancouver style is the subject of our blog today. This citation format is based on numbers wherein numbers are used in-text to indicate the author’s work. Then a corresponding list is created where the full citations are given, complete with the author’s name, the title of the source and other details. The rules of the Vancouver style have been developed by the International committee of Medical Journal Editors. 
				</p>
				<p class="text-font-sr">
					In this blog, we will give you the lowdown on the 2nd edition of the Vancouver style. Read on to know more. 
				</p>

				<p class="text-bold-sr">
					<h4><b>General notes on the referencing list in the Vancouver citation format</b></h4>
				</p>
				
				<p class="text-font-sr">
					Before you begin your reference list in the Vancouver citation format, you have to study its rules. These rules will teach you how to compose a reference list in this style without any mistakes. Take a look at the rules below- 
					<ul>
						<li>The reference list must begin on a new page after the rest of your assignment is over</li>
						<li>The title of the page should be ‘References’</li>
						<li>References should be entered in a numerical order following the order you have mentioned them in-text</li>
						<li>Your reference list should exclusively mention only those sources of information you have used in your assignment</li>
						<li>For numbering your reference list, use Arabic numerals only</li>
						<li>If you wish to abbreviate the titles of journals, you should use the style specified by the U.S. National Library of Medicine</li>
						<li>Maintain consistency throughout your assignment when referencing in the Vancouver style</li>
						<li>Cross-check your references to ensure you are maintaining the right order</li>
					</ul>
				</p>
				
				<p class="text-font-sr">
					Your reference list has to be completely accurate. If there’s even a single mistake in the numbering, you may end up losing precious grades. 
				</p>
				
				<p class="text-bold-sr">
					<h4><b>How you can cite different sources of information in the Vancouver format</b></h4>
				</p>

				<p class="text-font-sr">
					A citation is a way of showing that you acknowledge those work of others, which helped you in the creation of your assignment. It’s how you give credit where it’s due. In the Vancouver style, citing different sources of information requires the adherence to certain rules. Let’s find them out. 
				</p>

				<hr>
				<p class="text-bold-sr">
					<h4><b><i>
						In-text citation
					</i></b></h4>
				</p>

				<p class="text-font-sr">
					In-text citations are given when you either directly quote from someone else, paraphrase or admit to your views being influenced by someone else’s work. In the Vancouver style, in-text citations are given in the form of numbers which are put in brackets. These brackets can be either curved or square as long as you main consistency throughout your assignment. You can also use superscripts if you desire as long as they are in Arabic numerals only. If not in the middle of the text, reference numbers are generally put after a fullstop or comma. <br>
					E.g.: …this was what the government official had to state. (1)
					

				<p class="text-font-sr">
					If you use superscripts, you won’t have to put the numbers in brackets. 
				</p>
				
				<p class="text-font-sr">
					You can also cite with the author’s name.<br>
					E.g.: Schwartz 1 is of the opinion that…
				</p>

				<p class="text-font-sr">
					In the above case, the numerical has to be in superscript. 
				</p>
				<p class="text-font-sr">
					Alternatively, you can also mention page numbers in your in-text citations.<br>
					E.g.: …this is what the government official had to state. (1 p. 34)
				</p>
				<p class="text-font-sr">
					As you can see, Vancouver citations don’t end with periods. 
				</p>
				<p class="text-bold-sr">
					<h4><b><i>Citing quotations </i></b></h4>
				</p>
				<p class="text-font-sr">
					If you have borrowed quotes from another source and used them in your assignments, you have to first determine the number of lines the quotation is made up of. If it is 3 lines or lesser, you need to put the quote within quotation marks. <br>
					E.g.: “Referencing an assignment can be difficult. However, it is a challenge students must face if they wish to avoid plagiarism”. (1 p. 11)
				</p>
				<p class="text-font-sr">
					If the quote is more than 3 lines, start a new indented paragraph with the quote. Do not put it in quotation marks. <br>
					E.g.: 	Referencing an assignment can be difficult. However, it is a challenge students must face if they wish to avoid plagiarism. In case a student’s assignment lacks proper citation, he/she may be accused of plagiarism and given a failure grade. Plagiarism is a serious offence and can severely hurt the academic reputation, credibility as well as performance of the student concerned. (2 p. 11)
				</p>

				<p class="text-bold-sr">
				<h4><b><i>Citing paragraphed portions</i></b></h4>	
				</p>

				<p class="text-font-sr">
					To cite paragraphed portions in the Vancouver style, you simply need to mention the name of the author in the paragraph and then give the citation number.  <br>
					E.g.: Schwartz (3) is of the opinion that referencing should be taken seriously by anyone who wishes to produce research-based works.
				</p>

				<p class="text-font-sr">
					Note- Since we’ve already explained how in-text citations are done, from the next section onwards, we will give you the details on how to cite different sources in the reference list.  
				</p>

				<p class="text-bold-sr">
					<h4><b><i>Citing books</i></b></h4>
				</p>

				<p class="text-font-sr">
					For book citations, you have to give the name of the author, surname first, along with the title of the book, its edition, its place of publication, the name of the publisher and the year as well as the page number in this exact order. <br>
					E.g.: Mathews M. The Science Behind Referencing. 3rded. New Jersey: Smith Publishing; 2010. 520 p. If there’s more than a single author of the book concerned, mention the name of all authors in the order in which they appear on the cover of the book. 
.
				</p>
				<p class="text-font-sr">
					In the case of edited books, you will have to replace the name of the author with that of the editor. The rest of it remains the same.<br>
					E.g.: Kingsley P. Referencing: Your key to avoiding Plagiarism. New Jersey: Smith Publishing; 2011. 301 p. 
				</p>

				<p class="text-bold-sr">
					<h4><b><i>Citing e-books</i></b></h4>
				</p>

				<p class="text-font-sr">
					While the rest of the details remain more or less the same as a printed book, for an e-book you have to give the DOI (if available). Other things you have to mention include the date of access and the website URL along with the word ‘internet’ mentioned in square brackets. <br>
					E.g.: Kelly G. A Guide To Vancouver Referencing [Internet]. New York: Maze Books, 2013 [cited 12 October 2018]. 95 p. Available from https://www.ebooks.com/kelly-g
				</p>


				<p class="text-bold-sr">
				<h4><b><i>Citing Journals</i></b></h4>	
				</p>

				<p class="text-font-sr">
					In the case of journals, additional information that has to be added include the title of the article and that of the journal. The date of publication is mentioned but publishing house and location are not. <br>
					E.g.: Parker SD. Referencing at its best. The Citer. 2006 Feb; 63(1): 113-7.  
				</p>

				<p class="text-font-sr">
					If the journal has 2 to 6 authors you’ll have to give the name of all the authors with commas in between each of them. If there are more than 6 authors, mention the names of the first 6 and then give an ‘et al.’ for the rest. 
				</p>

				<p class="text-bold-sr">
					<h4><b><i>Citing newspapers</i></b></h4>
				</p>

				<p class="text-font-sr">
					Unlike journals where you can abbreviate the title, the title of a newspaper can never be abbreviated. You must give it in full. Other details that need to be entered include the edition of the newspaper, its date, the section, page number as well as the column. <br>
					E.g.: Mason, Douglas C. How Science Should Be Applied In Daily Life. 7 Oct. 2018:<a href="http://howtocitepapers.com/">https://www.howscienceworks.com/</a> 
				</p>
				<p class="text-font-sr">
					If the author of the article is not given, you can start straight with the title of the article keeping the rest of the details intact. 
				</p>

				<p class="text-bold-sr">
					<h4><b><i>Citing websites</i></b></h4>
				</p>
				<p class="text-font-sr">
					The first and foremost thing you need to keep in mind is that the word ‘internet’ will feature in square brackets in the citation for websites. The name of the author or the organization will have to be given as wellalongwith the title of the page, names of the place of publication and publisher, date of publication and the URL.<br>
					E.g.: World Referencing. How Referencing Helps Research [Internet]. Canberra: World Referencing; 2013 [updated 2013 July 16; cited 2014 Oct 17]. Available from: http://www.worldreferencing.com/referencing-research/
				</p>
				<p class="text-font-sr">
					That’s how you cite in the Vancouver style. It’s a very straightforward style of citation and is quite easy to follow. That said, unless you’re a student of science or medicine, you likely will not be asked to cite your assignments in this format. 

