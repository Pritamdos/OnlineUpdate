				<p class="text-font-sr">
					Plagiarism is a punishable offence. Not just in the world of music and media but in that of assignment writing as well. If you write an impeccable assignment but forget to or don’t have the time to reference it, you could seriously hurt your academic reputation. Professors hold the right to reject your paper and give you an F-grade in such a scenario. Hence, the importance of referencing academic papers cannot be denied.
				</p>
				
				<p class="text-font-sr">
					That said, there are plenty of referencing styles that are prescribed by different professors for different assignments. Today our focus is going to be the Harvard referencing style. The Harvard referencing style is also known as parenthetic referencing as every citation in this style is enclosed in parenthesis. Just like the APA style, it’s also based on the author-date system.	
				</p>

				<p class="text-font-sr">
					In this blog you will find everything you need to know about this citation style – right from its rules to how to use it.
				</p>
				
				<p class="text-bold-sr">
					<h4><b>The rules of making a reference list in the Harvard style</b></h4>
				</p>
				
				<p class="text-font-sr">
					Before we give you the details on how to cite different sources of information (newspapers, journals, books etc.) in the Harvard style, allow us to give you a lowdown on the creation of a reference list. The reference list in the Harvard style should include every single source of information you’ve used for the completion of your assignment. Here are the rules of the reference list-
					<ul>
						<li>Begin the reference list on a new page at the very end of your assignment</li>
						<li>Mention all the sources of information used in an alphabetical order, starting with the surname of the author instead of first name</li>
						<li>The name of the author will always come before the year</li>
						<li>In case the concerned piece of work you used does not have an author, begin with the title of the work; here too you should go alphabetically</li>
						<li>If you use multiple texts of the same author for your assignment, order them by their date of publication – the earliest ones come first</li>
						<li>However, in case two works of the same author have been published in the same year, add a lower case later right after the date of each work, beginning alphabetically</li>
						<li>The reference list should be double-spaced with a blank line between each source of information mentioned</li>
						<li>The full reference of every single in-text reference should be given at the end in the reference list</li>	
					</ul>
				</p>
				
				<p class="text-font-sr">
					As for indentations, consult your professor or go through your university rule book to know how much space you should leave on the left. Otherwise, these basic rules should be more than enough to help you reference accurately in the Harvard style.
				</p>

				<p class="text-bold-sr">
				<h4><b>How to cite different sources of information in the Harvard style</b></h4>	
				</p>

				<hr>

				<p class="text-font-sr">
					To complete a single assignment, you will have to go through a truckload of media. These could include journals, books, documentaries, newspapers and more. No matter which type of media you use for your assignment, you have to cite it in the right manner in the Harvard style. Merely making a list won’t do. Here’s how you should cite different sources of information in the Harvard style with examples- 
					
				</p>

				<p class="text-bold-sr">
					<h4><b><i>In-text citation</i></b></h4>
				</p>

				<p class="text-font-sr">
					Once you’re done paraphrasing or giving a quote from a particular source, you will have to cite it in-text. The way you cite it in the Harvard style will be dependent on the number of authors the said work has. 
				</p>

				<p class="text-font-sr">
					In case of a single author, you will have to enter the name of the author, the date and page number. <br>
					E.g.: (Marcus, 2016, p. 126)
				</p>
				
				<p class="text-font-sr">
					In case of two or more authors, both names have to be mentioned.<br>
					E.g.: (Marcus and Croydon, 2015, p. 125)
				</p>

				<p class="text-font-sr">
					In case of more than 4 authors, ‘et al’ will have to be mentioned after the name of the first author.<br>
					E.g.: (Marcus et al, 2014, p. 124)
				</p>
				<p class="text-font-sr">
					If no authors are mentioned of the given text, you will need to write the title of the concerned piece in italics. <br>
					E.g.: (Harvard Citation Guide, 2016, p.123)
				</p>
				<p class="text-font-sr">
					This kind of in-text citation applies to all kinds of sources you may have used for the completion of your assignment.
				</p>
				<hr>

				<p class="text-bold-sr">
				<h4><b><i>Book citation</i></b></h4>	
				</p>

				<p class="text-font-sr">
					To include the names of books in your reference list, you have to give the surname of the author, his/her initials, the year of publication, its title, edition as well as the location of publication.<br>
					E.g.: Marcus, J (2016) Harvard Citation Guide. 2ndedn. New York. New York Publisher.
				</p>

				<p class="text-font-sr">
					If the book contains content from multiple authors compiled by an editor, you will have to give the name of the editor instead of the author. <br>
					E.g. Cosby, D.S. (eds.) (2014) Citation: A complete guide to rules of referencing. New York. New York Publisher.
				</p>

				<p class="text-bold-sr">
					<h4><b><i>E-book citation</i></b></h4>
				</p>

				<p class="text-font-sr">
					E-book citations are slightly different from those of printed books in the Harvard style. The DOI (date of access) or the URL or both have to be added right at the end after the rest of the details have been mentioned. <br>
					E.g.: Marcus, J (2016) Harvard Citation Guide. Online Edition [online].
				</p>

				<p class="text-font-sr">
					Available at: <a href="http://www.referencing.com/reference-manager">http://www.referencing.com/reference-manager</a> (Accessed: 6 October 2018)
				</p>

				<p class="text-bold-sr">
					<h4><b><i>Journal citation</i></b></h4>
				</p>

				<p class="text-font-sr">
					To cite a journal article, the title of the article has to be mentioned in single quotation marks.<br>
					E.g.: Marcus, J ‘How citation can save you from plagiarism’, The Citer, 61(8), p. 69-70.<br>
					Here the name of the journal should be italicized.
				</p>

				<p class="text-font-sr">
					In case you’ve referred to an online journal for your assignment, the date of access has to be given along with the word ‘online’ in third brackets to denote the information was taken from the internet. <br>
					E.g.: Marcus, J ‘How citation can save you from plagiarism’, The Citer, 61(8) [online]. Available at: <a href="http://www.referencing.com/reference-manager"> http://www.referencing.com/reference-manager</a> (Accessed: 6 October 2018) 
				</p>

				<p class="text-bold-sr">
				<h4><b><i>Newspaper citation</i></b></h4>	
				</p>

				<p class="text-font-sr">
					The citation of journals and newspapers in the Harvard style is exactly the same. The only exception is the title of the journal is replaced by the title of the newspaper concerned. <br>
					E.g.: Marcus, J ‘Un-referenced papers to receive a major blow’, The Citer Telegraph (Weekend edition), 6 October, pp. 5-6.
				</p>

				<p class="text-font-sr">
					For an online newspaper, you will have to add in the word ‘online’ in third brackets as well as give the URL and the DOI at the end. <br>
					E.g.: Marcus, J ‘Un-referenced papers to receive a major blow’, The Citer Telegraph (Weekend edition), 6 October, [online]. Available at <a href="http://www.referencing.com/reference-manager"> http://www.referencing.com/reference-manager </a>(Accessed: 6 October 2018).
				</p>

				<p class="text-font-sr">
					The page number in the case of online newspapers need not be mentioned.
				</p>

				<p class="text-bold-sr">
					<h4><b><i>Website citation</i></b></h4>
				</p>

				<p class="text-font-sr">
					The basic format of citing websites in the Harvard style is pretty straightforward. You have to give the usual details such as the author and publishing year of the title along with the page title or the name of the website, the URL and the DOI. <br>
					E.g.: Marcus, J (2016) How to reference accurately each time [Online]. Available at: <a href="http://howtocitepapers.com/">http://howtocitepapers.com/</a> (Accessed: 6 October 2018). 
				</p>

				<p class="text-font-sr">
					Just be careful to keep the name of the article concerned in italics.
				</p>
				<p class="text-bold-sr">
					<h4><b><i>Quotation citation</i></b></h4>
				</p>
				<p class="text-font-sr">
					To cite direct quotations, you will have to put the sourced text in double quotation marks and then follow it up with the details of the author. <br>
					E.g.: Marcus points out, “Citing an academic paper is like creating a piece of art. You have to be meticulous, thorough and pay attention to every single detail” (Marcus, 2016, p. 90). 
				</p>
				<p class="text-font-sr">
					For paraphrased quotes, you can follow the example given above of in-text citations. There are no changes there. 
				</p>
				<p class="text-bold-sr">
					<h4><b><i>Citing other sources in Harvard style</i></b></h4>
				</p>
				<p class="text-font-sr">
					Picture: Pictures taken from online sources have to have the name of the photograph mentioned along with the photographer. <br>
					E.g.: Carter, E. (1991) Maria [online]. Available at:<a href="www.artwork.org/carter-maria"> www.artwork.org/carter-maria</a> (Accessed: 6 October 2018)
				</p>
				<p class="text-font-sr">
					Movie: The name of the movie will come first followed by the director of the movie. <br>
					E.g.: Pulp Fiction (1994) Directed by Quentin Tarantino [Film]. Los Angeles: Miramax Films.
				</p>
				<p class="text-font-sr">
					Television show: To cite television shows, you have to give the name of the episode followed by the year of broadcast, name of the show, number of season and episode and the name of the channel. <br>
					E.g.: ‘Fly’ (2010) Breaking Bad, Series 2, episode 10. AMC, 23 May 2010.
				</p>
				<p class="text-font-sr">
					That’s all you need to know about the Harvard citation style. You can keep this list handy and use it every time you face doubts citing in the Harvard referencing format. Citing accurately in this format will definitely land you a good grade. 
				</p>
