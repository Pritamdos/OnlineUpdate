				<p class="text-font-sr">
					If you are going to research for your assignment, you will have to reference it. That’s the simple rule. There’s no escaping it. Not giving credit where it’s due can land you in trouble. So even if you hate the task of referencing, you will have to buckle up and do it.
				</p>
				
				<p class="text-font-sr">
					Having previously covered the Harvard and APA referencing styles in our blogs, today we’re going to tell you all about the rules of the MLA style. Currently in its 8th edition, the MLA referencing format was developed by the Modern Language Association. In this blog, we will reveal to you all the rules of the MLA 8 citation style so you can do it right when you reference your assignment.
				</p>

				<p class="text-bold-sr">
					<h4><b>The core formula of the MLA referencing style</b></h4>
				</p>
				
				<p class="text-font-sr">
					The MLA citation format is much easier in comparison to the rest of them. That said, it has its own set of rules, which have to be adhered to if you want to get this style right. Here’s a list of the core rules of this citation style-
					<ul>
						<li>When giving the name of the author, the surname has to be mentioned first, then a comma and then the first name</li>
						<li>The title of the work given needs to be italicized</li>
						<li>Online user names and pseudonyms can be used in place of the real name of the author if not known</li>
						<li>In case an article has been taken from a book, the name of the book has to be mentioned in double quotes before the title of the article</li>
						<li>The version/edition, volume, name of publisher, date of publication and location of publication have to be given</li>
						<li>If you can’t find the exact date of the publication of the source, mention ‘circa’ before the approximate date</li>
						<li>A question mark needs to be added if you’re not sure of the information given of the source</li>
						<li>Whatever is not a part of the original source should be put in third brackets</li>
					</ul>
				</p>
				
				<p class="text-bold-sr">
					These rules apply to every kind of source of information you intend to cite in your assignment.
				</p>
				
				<p class="text-bold-sr">
					<h4><b>The rules of the MLA referencing list </b></h4>
				</p>

				<p class="text-font-sr">
				Now that we’ve got the basic rules out of the way, let’s talk about the rules you need to stick to when creating the reference list in the MLA style. The rules are very basic and easy to follow-
				<ul>
					<li>The reference list must begin on a new page at the end of the assignment</li>
					<li>Name of the authors should be mentioned in alphabetical order on the basis of the first name of the author</li>
					<li>If the name of the author is not given, the title needs to be mentioned first in the list in alphabetical order</li>
					<li>If you use multiple works of the same author, they should be mentioned chronologically i.e., the earliest published first</li>
					<li>Every single entry that goes into the list should be double spaced</li>
					<li>In case you list multiple works of the same author, you need to give the full name of the author only for the first reference; in subsequent references, author name will be replaced with ‘- - -‘</li>
					<li>References should be given in full for all in-text citations</li>
				</ul>
				</p>

				<hr>

				<p class="text-font-sr">
					The above rules may seem overwhelming but once you practice citing in this style, they will come naturally to you.
					
				</p>

				<p class="text-bold-sr">
					<h4><b>How to cite different sources in the MLA style</b></h4>
				</p>

				<p class="text-font-sr">
					This is the main section of our blog and here we are going to tell you how to cite different sources using the MLA style. We’ll give in-depth examples along with instructions so you get a fair idea of how to do MLA citations without errors. 
				</p>

				<p class="text-bold-sr">
					<h4><b><i>In-text citation</i></b></h4>
				</p>
				
				<p class="text-font-sr">
					In-text citations in the MLA style are very straightforward. Whether you paraphrase or quote, you simply have to mention the name of the author along with the page number. <br>
					E.g.: (Mason 180)
				</p>

				<p class="text-font-sr">
					This applies for direct quotes or as well as paraphrased portions. However, if you wish to quote directly, you can give the author name first followed by the page number in brackets. .<br>
					E.g.: Mason points out, “…” (180)
				</p>
				<p class="text-font-sr">
					If you’ve borrowed from a work that has more than one author, you will need to mention individual names. <br>
					E.g.: (Mason and Harrington 180)
				</p>
				<p class="text-font-sr">
					However, if there are more than 3 authors, only the name of the first author followed by an ‘et al’ has to be written. The ‘et al’ denotes the rest of the authors. <br>
					E.g.: (Mason et al. 180)
				</p>
				<p class="text-font-sr">
					If there are no authors given for the work you’ve borrowed from, start with the title and italicize it.<br>
					E.g.: (The Book Of Science 169)
				</p>
				<p class="text-font-sr">
					If the authors you’ve referred to have the same surname, mention the initial of their first name to avoid confusion.<br>
					E.g.: (K. Mason 180) and (D. Mason 191)
				</p>
				<p class="text-font-sr">
					If the page number has not been given, mention the chapter number or paragraph number.<br>
					E.g.: (Mason, ch. 8)
				</p>
				<p class="text-font-sr">
					An alternative way of giving direct quotes from texts is to mention ‘qtd.’ In the brackets.<br>
					E.g.: (qtd. In Mason 180)
				</p>

				<p class="text-bold-sr">
				<h4><b><i>Book citation</i></b></h4>	
				</p>

				<p class="text-font-sr">
					The citation of books in the MLA style follows the basic order of this format, which means the name of the author comes before anything else. The title of the book, its edition, publisher and year of publication have to be put down as well. <br>
					E.g.: Mason, Douglas C. The Book of Science. 3rd ed, New York Publisher, 2016.
				</p>

				<p class="text-font-sr">
					The name of the book should be in italics as given in the example above. 
				</p>
				<p class="text-font-sr">
					To cite edited books, you need to give the name of the editor along with the name of the author. <br>
					E.g.: Taylor, Max S., editor, and Maria James. The Great Gastronomy. LA Publishing, 2014. 
				</p>

				<p class="text-bold-sr">
					<h4><b><i>E-book citation</i></b></h4>
				</p>

				<p class="text-font-sr">
					The citation of e-books is not much different from that of paper books in the MLA format. Simply mentioning the fact that it’s an e-book along with the rest of the details will do. <br>
					E.g.: Mason, Douglas C. The Book of Science. 3rd ed, e-book, New York Publisher, 2016.
				</p>

				<p class="text-bold-sr">
					<h4><b><i>Essay citation</i></b></h4>
				</p>

				<p class="text-font-sr">
					Essay citation is different from citing a book. However, the differences are only minor. You just have to give the name of the essay before the name of the book it’s a part of. <br>
					E.g.: Mason, Douglas C. “Earth and Everything”. The Book of Science, New York Publisher, 2016, pp. 101-102.
				</p>

				<p class="text-font-sr">
					Keep the name of the essay in quotes and give their page numbers too. The same rules apply for citing a chapter.  
				</p>

				<p class="text-bold-sr">
				<h4><b><i>Article citation</i></b></h4>	
				</p>

				<p class="text-font-sr">
					By articles here we mean those derived journals, magazines, newspapers as well as online sources. The format of citing articles from each of them is more or less the same. The date and title have to be mentioned. 
				</p>

				<p class="text-font-sr">
					Let’s begin with the example of a journal.<br>
					E.g.: Mason, Douglas C. “What Science Has Taught Us”. The Scientific Journal, vol. 5, no. 7, October 2018, pp. 405-406. 
				</p>

				<p class="text-font-sr">
					The way a newspaper can be cited in the MLA style is as follows-<br>
					E.g.: Mason, Douglas C. “How Scientific Discoveries Changed The World.” The Science Graph, Weekend Edition, vol. 66, no. 10, October 2018, pp. 12-13. 
				</p>
				<p class="text-font-sr">
					Here’s how you can reference an online article in the MLA format- <br>
					E.g.: Mason, Douglas C. “Science: The Religion We Need”. The Scientific Journal, vol. 5, no. 8, October 2018, Journal Database,<a href="http://www.thescientific.com/science-the-new-religion"></a> http://www.thescientific.com/science-the-new-religion </p><br>
					The website URL has to be given when you enter an online article in your reference list. 

				</p>
				<p class="text-font-sr">
					In each of them, you will have to put in the respective volumes and editions of the sources you gained the concerned information from. 
				</p>

				<p class="text-bold-sr">
					<h4><b><i>Website citation</i></b></h4>
				</p>

				<p class="text-font-sr">
					Just like online articles, referencing websites in the MLA format will require you to mention the date of access as well as the URL.<br>
					E.g.: Mason, Douglas C. How Science Should Be Applied In Daily Life. 7 Oct. 2018:<a href="http://howtocitepapers.com/">https://www.howscienceworks.com/</a> 
				</p>

				<p class="text-bold-sr">
					<h4><b><i>Citing non-print material in the MLA format </i></b></h4>
				</p>
				<p class="text-font-sr">
					Non-print materials such as movies, TV shows and music have their own rules of citation in the MLA format. Let’s discuss them one by one. 
				</p>
				<p class="text-font-sr">
					Movies: For movies, the director’s name will come first followed by the movie’s name, its actors, the distributor and the year of release.<br>
					E.g.: Reiner, Rob, director. “When Harry Met Sally”. Performed by Meg Ryan and Billy Crystal, Columbia Pictures, 1989. DVD. 

				</p>
				<p class="text-font-sr">
					TV show: In the case of TV shows, the name of the episode has to be given before anything else. Other things that have to be mentioned include the name of the show, its writer and director, season and episode number, air date and channel.<br>
					E.g.: “The Men of Always”. Narcos, Dana Calvo, directed by Guillermo Navarro, Season 1, Episode 3, Netflix, 2015.
				</p>
				<p class="text-font-sr">
					Music: The name of the artist comes first and then the name of the song followed by album name, the production company and the year. <br>
					E.g.: Michael Jackson. “Beat It”. Thriller, Epic, 1982. 
				</p>
